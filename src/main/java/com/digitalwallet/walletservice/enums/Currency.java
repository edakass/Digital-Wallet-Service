package com.digitalwallet.walletservice.enums;

/**
 * Enum representing the supported currency types for wallets and transactions.

 * This enum is used to define and restrict acceptable currency values in the system.
 */
public enum Currency {

    /**
     * Turkish Lira
     */
    TRY,

    /**
     * United States Dollar
     */
    USD,

    /**
     * Euro
     */
    EUR
}